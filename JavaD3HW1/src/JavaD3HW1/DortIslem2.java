package JavaD3HW1;

public class DortIslem2 {
	public int topla(int sayi1, int sayi2) {
		return sayi1 + sayi2;
	}
	
	public int topla2(int sayi1, int sayi2, int sayi3) {
		return sayi1 + sayi2 + sayi3;
	}
	
	// ikisine de topla diyebilirdik, çünkü ikisinin de metot parametrelerinin imzası farklı.
	// Buna metot overloading denir. 
}
